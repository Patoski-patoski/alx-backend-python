#!/usr/bin/env python3
"""2-floor.py: function to_str that takes a float n as argument and returns the
   string representation of the float.
"""



def floor(n: float) -> int:
    """Get the value of the floor of the float.

    Args:
        n (float): floating number

    Returns:
        float: returns the floor of the float.
    """
    from math import floor
    return floor(n)
