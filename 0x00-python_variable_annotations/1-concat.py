#!/usr/bin/env python3
"""1-concat.py:  function concat that takes a string str1 and a string str2 as
   arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings

    Args:
        str1 (str): The first string to be added.
        str2 (str): The first string to be added.

    Returns:
        str: a concatenated string
    """
    return str1 + str2
