#!/usr/bin/env python3
"""3-to_str.py: function to_str that takes a float n as argument and returns
  the string representation of the float.
"""


def to_str(n: float) -> str:
    """Get the string representaion of float value

    Args:
        n (float): the number to convert to string

    Returns:
        str: returns the string representation of the float.
    """
    return str(n)
